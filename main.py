#!/usr/bin/env python3
# -*- coding: utf-8 -*-
# =============================================================================
# main.py - ゲームのエントリーポイント（起動ファイル）
# =============================================================================
#
# 【このファイルについて】
# このファイルはゲームを起動するための「入り口」です。
# ターミナル（コマンドプロンプト）から以下のコマンドで実行できます：
#
#   python main.py
#
# 【このファイルで学べること】
# - Pythonプログラムのエントリーポイント
# - モジュールのインポート方法
# - if __name__ == "__main__" の意味と使い方
#
# =============================================================================

# 他のモジュール（ファイル）をインポート
# game パッケージ（フォルダ）内の game_manager モジュールから
# GameManager クラスをインポートしています
from game.game_manager import GameManager


def main():
    """
    メイン関数 - ゲームを開始する
    
    【解説】
    プログラムの開始点となる関数です。
    慣習的に main() という名前を付けることが多いですが、
    別の名前でも問題ありません。
    
    【実行内容】
    1. GameManager のインスタンスを作成
    2. ゲームのメインループを開始
    
    【なぜ関数にまとめるのか？】
    - コードの整理: 何を実行するか明確になる
    - テストしやすさ: 関数単位でテストできる
    - 再利用性: 他のプログラムからも呼び出せる
    """
    # GameManager クラスのインスタンスを作成
    # GameManager() と書くことで、新しいゲームマネージャーが作られます
    game = GameManager()
    
    # ゲームのメインループを開始
    # run() メソッドはゲームが終了するまで戻ってきません
    game.run()


# =============================================================================
# プログラムのエントリーポイント
# =============================================================================
#
# 【 if __name__ == "__main__": の解説】
#
# __name__ は Python の特殊変数で、現在のモジュール名を保持しています。
#
# - このファイルを直接実行した場合（python main.py）:
#   __name__ は "__main__" になります
#
# - このファイルが他のファイルからインポートされた場合:
#   __name__ は "main" (ファイル名から拡張子を除いたもの) になります
#
# つまり、この if 文は「このファイルが直接実行されたときだけ
# 以下のコードを実行する」という意味になります。
#
# 【なぜこれが必要なのか？】
# モジュールをインポートしただけでゲームが始まってしまうと困ります。
# 例えば、テストを実行するときにゲームが始まってしまうと
# テストが正常に動きません。
# この if 文により、直接実行したときだけゲームが始まります。
#
# =============================================================================

if __name__ == "__main__":
    # このファイルが直接実行された場合のみ、main() 関数を呼び出す
    main()
